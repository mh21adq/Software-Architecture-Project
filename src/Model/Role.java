package Model;

/**
 * The Role enum represents various roles that can be assigned to individuals in the system.
 * These roles determine their permissions and responsibilities within the system.
 */
public enum Role {
    /**
     * Represents the role of a Data Entry personnel, responsible for entering data into the system.
     */
    DATA_ENTRY,

    /**
     * Represents the role of an Officials personnel, responsible for managing official tasks in the system.
     */
    OFFICIALS,

    /**
     * Represents the role of an Emergency Response personnel, responsible for handling emergency situations.
     */
    EMERGENCY_RESPONSE,

    /**
     * Represents the role of a Referee, responsible for officiating and making decisions in events.
     */
    REFEREE
}
